  while (1)
  {
	  /*
		PB0 is CE (Chip Enable)
		PB1 is CSN (Chip Select Not)
		PA8 is IRQ
		PA7 is MOSI input
		PA6 is MISO
		PA5 is SCK (System Clock)

		PB4 Buzzer output
	 */

	  // If the there is available data keep pushing it to the Morse array until the terminating character of five is included.
	  // Once 5 has been read convert the relevant parts of the stream to the MorseToSignal function which should output data from the buzzer
	  if (isDataAvailable(2) == 1){
		  uint8_t RxData[packetLength];
		  NRF24_Receive(RxData);

		  if(containsCharacter((char *)RxData, packetLength, '5')){
			  int index = findIndexChar('5', morse, strlen(morse));

			  morse = realloc(morse, strlen(morse) + (index * sizeof(char)) + 1);

			  // To add the last values to the end of the Morse array we create a points that points to it and then appends the RxData array up till
			  // the index of the Morse Terminating Character (5)
			  memcpy(morse + strlen(morse), RxData, index);

			  beepRunning = 1;
		  }else{
			  morse = realloc(morse, strlen(morse) + (packetLength * sizeof(char)) + 1);
		  	  strcat(morse, (char*)RxData);

		  	  //Sets final value of the Morse array to the string terminating character
		  	  morse[strlen(morse)] = '\0';
		  }

		  if(beepRunning == 1){
			  beepComplete = morseToSignal(morse, strlen(morse), GPIOB, GPIO_PIN_4);

			  if(beepComplete == 1){
				  beepRunning = 0;
			  }

		  }


	  }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
